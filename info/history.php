<h1>History of the Quest</h1>

<h3>0T3</h3>

<p>The puzzles in this Quest were all cryptographic in nature. In order to
    claim the prize however, the winner had to sing Bust a Move at Eglinton
    Station.</p><br/>

<h3>0T4</h3>

<p>This Quest incorporated music trivia and word searches into puzzles. The
    winner had to perform The Tale of Mr. Morton by Schoolhouse Rock near the
    Union subway station. They brought some instruments down and played during
    the evening rush.</p><br/>

<h3>0T5</h3>

<p>The Quest this year was styled after the 'Keeper of the Bridge of Death',
    from Monty Python and the Holy Grail. It was 5 puzzles, including a
    euchre-based puzzle and a question about the air-speed velocity of a swallow.
    This was the last purely text-based quest.</p><br/>

<h3>0T6</h3>

<p>Up to this point, the Quest was just a bunch of puzzles thrown together.
    This year the puzzles were all linked together with an overall story, taking
    participants to various buildings around campus in the story. The order the
    buildings were visited was the solution key to the final puzzle. This was the
    last year where participants would bribe the Quest Master for hints to the
    puzzles. Some of the puzzles required participants to piece together further
    instructions from the text on landmarks, such as the 0T5 wall in the SF
    atrium which had the verses of Godiva's Hymn.</p><br/>

<h3>0T7</h3>

<p>This year's Quest followed suit from the previous year's, using an overall
    story to link the puzzles together. However, with the graphics help of Henry
    Cheung (B&G co-chair with John McLeod that year, and the designer of the
    still-used godivaweek.skule.ca layout), each puzzle was stored as an image,
    and additional visual clues were embedded in the image. This Quest led
    participants around campus, and each puzzle solution was a piece of the final
    puzzle, which required participants to find a specific book in the SF library
    to win.</p><br/>

<h3>0T8</h3>

<p>This Quest was unique in that no one knew who the Quest Masters were until
    the final puzzle. The Quest was run as an ARG, where participants got
    instructions from an unknown person, identified as 'Godgifu of Mercia' a
    couple puzzles in. This is the first Quest to use instant-feedback for
    correct answers, as well as IP logging to detect cheating, and puzzles built
    in Flash. It featured 14 online puzzles (more than double the amount of most
    years), and a run-around portion where participants got information on where
    and when the final puzzle was. Puzzles ranged from simple decryption to
    piecing together images on the plasma screen. At one point participants had
    to answer a public phone at a certain time, and were given instructions from
    a pre-recorded computer message.</p><br/>

<h3>0T9</h3>

<p>Without a doubt, this was one of the nicest looking Quests ever made. It
    also made huge improvements to the back-end. Up until this point registration
    was dealt with by the Quest Master. This Quest had automated user
    registration, generating aliases for each participant to be used when
    submitting answers. On top of that, you could see which users had solved what
    puzzles on their automatically updated scoreboard. There were 16 puzzles to
    be solved, using a wide variety of puzzle types. The first 9 puzzles could be
    solved in any order, however the 9th puzzle was easier after solving the
    first 8. Clues to help solve the 9th were emailed to the participants once
    they solved each of the puzzles leading up to the 9th. Upon solving each
    puzzle in the last batch of puzzles, participants were emailed a section of a
    lock combination. The final puzzle gave the location of the locker, and
    inside were instructions to phone the QuestMaster to claim victory.</p><br/>

<h3>1T0</h3>

<p>This Quest returned to a linear model, and was similar to online
    challenges like the Tim Tang Test and notpr0n. Registration returned to the
    traditional method of filling out a form, and user progress was not tracked.
    Puzzles included hidden text in images, clever riddles, and a clue inside the
    Beast.</p><br/>

<h3>1T1</h3>

<p>This Quest was run by Evangelos Staikos, who had already previously co-run
    the 0T8 Quest. The back-end reused 0T9 code allowing for automatic
    registration. The puzzles were built in Flash. There were 26 puzzles in total
    this year, the most of any quest to date by far. The puzzles were organized
    into 3 stages and included a clicking escape puzzle. There was no meta-puzzle
    and the winner had to solve all 26 puzzles to win.</p><br/>

<h3>1T2</h3>

<p>The back-end reuses 1T1 code and much of the styling is reused. There are
    23 puzzles in total arranged in 3 stages. Automatic emailing has been
    disabled (I couldn't get it to work), so batch emails will be sent to notify
    new stages being unlocked. Meta-puzzles are reintroduced in this
    quest.</p><br/>

<h3>1T3</h3>

<p>The back-end reuses 1T1 code again. The Quest started on December 21,
    2012, the alleged end of the world. Therefore, the theme this year is to find
    the Godiva's Ark, which has a similar function as the Noah's Ark (to prevent
    the world from ending, basically). The Godiva's Ark is hidden in one of the
    Engineering buildings and the first person that finds the Ark will be the
    winner. There are 3 stages, 21 puzzles in total. Will you find the Ark to
    save us all?</p>

<h3>1T4</h3>

<p>Once again using the lovely 1T1 backend and style, this year's Quest
    contains 21 puzzles arranged into three phases. Designed for the first time
    by a Chem, the puzzles are meant to move away from a computer and cryptology
    basis, and are based on problem-solving, logic, and maybe a little dumb
    luck.</p>

